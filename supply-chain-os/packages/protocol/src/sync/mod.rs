/// CRDT sync module — device-to-device synchronization via Automerge.
///
/// Purpose: When a user has both a desktop node and a mobile node (same identity),
/// they need their local SQLite projections to stay in sync without a central server.
///
/// Strategy:
/// - The source chain is the authority. Automerge syncs the *projected state* (SQLite read model).
/// - Each device maintains an Automerge document representing mutable projected state.
/// - Devices exchange Automerge sync messages over a direct libp2p connection.
/// - Conflict resolution is automatic (CRDT semantics — last-write-wins per field,
///   or set-union for collections like task queues).
///
/// This module is intentionally separate from the DAG module:
/// - DAG = immutable event log (source of truth)
/// - Sync = mutable projected state (fast read model, rebuildable from DAG)

use thiserror::Error;

#[derive(Debug, Error)]
pub enum SyncError {
    #[error("automerge error: {0}")]
    Automerge(String),
    #[error("sync peer not found: {0}")]
    PeerNotFound(String),
    #[error("incompatible protocol version: expected {expected}, got {got}")]
    VersionMismatch { expected: String, got: String },
}

/// Sync message exchanged between a user's own devices.
///
/// These are generated by Automerge's sync protocol and carried over the
/// libp2p transport. They are NOT cross-node messages (those go through the DAG).
#[derive(Debug, Clone)]
pub struct SyncMessage {
    /// DID of the sending device
    pub from_did: String,
    /// DID of the target device (must share the same root identity keypair)
    pub to_did: String,
    /// Raw Automerge sync bytes
    pub payload: Vec<u8>,
    /// Unix ms
    pub timestamp: i64,
}

