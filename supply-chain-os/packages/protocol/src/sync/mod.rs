/// Sync session between two of the same user's devices.
pub struct DeviceSyncSession {
    pub local_did: String,
    pub remote_did: String,
    pub established_at: i64,
}

impl DeviceSyncSession {
    pub fn new(local_did: String, remote_did: String) -> Self {
        Self {
            local_did,
            remote_did,
            established_at: chrono::Utc::now().timestamp_millis(),
        }
    }
}
